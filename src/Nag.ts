export function renderNag(nag: string): string {
    switch (nag) {
        case '$1':
            return '!';
        case '$2':
            return '?';
        case '$3':
            return '!!';
        case '$4':
            return '??';
        case '$5':
            return '!?';
        case '$6':
            return '?!';
        case '$7':
            return '□';
        case '$10':
            return '=';
        case '$11':
            return '=';
        case '$12':
            return '=';
        case '$13':
            return '∞';
        case '$14':
            return '⩲';
        case '$15':
            return '⩱';
        case '$16':
            return '±';
        case '$17':
            return '∓';
        case '$18':
            return '+−';
        case '$19':
            return '−+';
        case '$22':
            return '⨀';
        case '$23':
            return '⨀';
        case '$26':
            return '○';
        case '$27':
            return '○';
        case '$32':
            return '⟳';
        case '$33':
            return '⟳';
        case '$36':
            return '↑';
        case '$37':
            return '↑';
        case '$40':
            return '→';
        case '$41':
            return '→';
        case '$44':
            return '=∞';
        case '$45':
            return '=∞';
        case '$132':
            return '⇆';
        case '$133':
            return '⇆';
        case '$138':
            return '⨁';
        case '$139':
            return '⨁';
        case '$140':
            return '∆';
        case '$146':
            return 'N';
    }

    return '';
}
